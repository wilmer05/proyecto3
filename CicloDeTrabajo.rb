class CicloDeTrabajo
  # Cantidad de ciclos.
  attr_reader :numeroCiclo

  # Construye una instancia de un CicloDeTrabajo
  #
  # ==== Atributos
  #
  # * +numeroCiclo+ - Cantidad de ciclos
  #
  def initialize
    @numeroCiclo = 0
  end
  
  def AumentarDeCiclo()
    @numeroCiclo += 1
  end
 
  
end